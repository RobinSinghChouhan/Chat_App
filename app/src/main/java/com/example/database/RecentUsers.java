package com.example.database;

public class RecentUsers {
    String id;
    String email;

    public RecentUsers(){

    }

    public RecentUsers(String id, String email) {
        this.id = id;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
